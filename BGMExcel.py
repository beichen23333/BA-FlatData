
import flatbuffers
from flatbuffers.compat import import_numpy
np = import_numpy()

class BGMExcel:
    __slots__ = ['_tab']

    @classmethod
    def GetRootAs(cls, buf, offset=0):
        n = flatbuffers.encode.Get(flatbuffers.packer.uoffset, buf, offset)
        x = BGMExcel()
        x.Init(buf, n + offset)
        return x

    def Init(self, buf, pos):
        self._tab = flatbuffers.table.Table(buf, pos)


    def Id(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(4))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def NationLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(6))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def PathLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(8))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def VolumeLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(10))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def LoopStartTimeLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(12))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def LoopEndTimeLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(14))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def LoopTranstionTimeLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(16))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0


    def LoopOffsetTimeLength(self):
        o = flatbuffers.number_types.UOffsetTFlags.py_type(self._tab.Offset(18))
        if o != 0:
            return self._tab.Get(flatbuffers.number_types.Int32Flags, o + self._tab.Pos)
        return 0




    @staticmethod
    def Start(builder): builder.StartObject(8)
    @staticmethod
    def End(builder): return builder.EndObject()


    @staticmethod
    def AddId(builder, Id): builder.PrependInt32Slot(0, Id, 0)


    @staticmethod
    def AddNationLength(builder, NationLength): builder.PrependInt32Slot(1, NationLength, 0)


    @staticmethod
    def AddPathLength(builder, PathLength): builder.PrependInt32Slot(2, PathLength, 0)


    @staticmethod
    def AddVolumeLength(builder, VolumeLength): builder.PrependInt32Slot(3, VolumeLength, 0)


    @staticmethod
    def AddLoopStartTimeLength(builder, LoopStartTimeLength): builder.PrependInt32Slot(4, LoopStartTimeLength, 0)


    @staticmethod
    def AddLoopEndTimeLength(builder, LoopEndTimeLength): builder.PrependInt32Slot(5, LoopEndTimeLength, 0)


    @staticmethod
    def AddLoopTranstionTimeLength(builder, LoopTranstionTimeLength): builder.PrependInt32Slot(6, LoopTranstionTimeLength, 0)


    @staticmethod
    def AddLoopOffsetTimeLength(builder, LoopOffsetTimeLength): builder.PrependInt32Slot(7, LoopOffsetTimeLength, 0)

